package com.chenjw.textimage.config;


/**
 * 用于指定文本位置
 * 
 * @author chenjw
 * 
 */
public class TextPosition {
	// x轴偏移位置
	private int x;
	// y轴偏移位置
	private int y;

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

}
