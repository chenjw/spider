package com.chenjw.textimage.service.model;

/**
 * 文字内容与边框之间的边距
 * 
 * @author chenjw
 * 
 */
public class Padding {
	private int left = 0;
	private int right = 0;
	private int top = 0;
	private int bottom = 0;

	public int getLeft() {
		return left;
	}

	public void setLeft(int left) {
		this.left = left;
	}

	public int getRight() {
		return right;
	}

	public void setRight(int right) {
		this.right = right;
	}

	public int getTop() {
		return top;
	}

	public void setTop(int top) {
		this.top = top;
	}

	public int getBottom() {
		return bottom;
	}

	public void setBottom(int bottom) {
		this.bottom = bottom;
	}

}
