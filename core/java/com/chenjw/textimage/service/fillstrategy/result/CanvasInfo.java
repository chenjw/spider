package com.chenjw.textimage.service.fillstrategy.result;

/**
 * 画布信息描述
 * 
 * @author chenjw
 * 
 */
public class CanvasInfo {

	private int width;
	private int height;

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

}
